#WORKER EFFICIENCY
Time= float(input('Input the time taken by the worker :'))
if(Time>=2 and Time<=3):
    print('Worker is highly efficient')
elif(Time>=3 and Time<=4):
    print('Worker needs to improve speed')
elif(Time>=4 and Time<=5):
    print('Worker should be given training to improve his speed')
elif(Time>5):
    print('Worker should leave the company')